package main

import "fmt"

func main() {
	// Using the following operators, write expressions and
	// assign their values to variables:
	// ==
	// <=
	// >=
	// !=
	// <
	// >

	a2 := (99 == 99)
	b2 := (99 <= 25)
	c2 := (99 >= 25)
	d2 := (99 != 99)
	e2 := (99 < 100)
	f2 := (99 > 100)

	fmt.Println(a2, b2, c2, d2, e2, f2) // Result: true, false, true,
	// false, true, false
}
